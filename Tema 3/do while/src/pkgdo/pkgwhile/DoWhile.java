package pkgdo.pkgwhile;

/**
 *
 * @author Usuario
 */
public class DoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int i = 1;
        
        do{
            System.out.println(i*i);
            i++;
        }while (i<=10);
    }
    
}
